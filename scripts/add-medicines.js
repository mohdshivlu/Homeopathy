async function addMedicines() {
  try {
    console.log(`Adding ${medicines.length} medicines to database...\n`);
    let successCount = 0;
    let errorCount = 0;

    for (let i = 0; i < medicines.length; i++) {
      const medicine = medicines[i];
      try {
        await db.Medicine.create(medicine);
        successCount++;
        console.log(`${i + 1}. ✅ ${medicine.name} - Added successfully`);
      } catch (error) {
        errorCount++;
        console.log(`${i + 1}. ❌ ${medicine.name} - Error: ${error.message}`);
      }
    }

    console.log(`\n=== Summary ===`);
    console.log(`Total: ${medicines.length}`);
    console.log(`Success: ${successCount}`);
    console.log(`Errors: ${errorCount}`);
    console.log(`\nDone!`);

    process.exit(0);
  } catch (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }
}

addMedicines();
